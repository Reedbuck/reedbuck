            <div class="col-xs-12 col-sm-12 col-md-12 servises_opacyty_block" style="display:none;"><h2>Datenverschlüsslung</h2> 
               <div class="col-xs-12 col-sm-12 col-md-12">
                <p>
                    <img src="http://www.computerwelt-am-rhein.de/img/service.jpg" align="left" vspace="10" hspace="20" alt="© abcmedia - Fotolia.com" border="0">
                    Das Ziel einer Verschlüsselung liegt darin, Daten in mathematischen Transformationen zu unterwerfen, sodass ein eventueller Angreifer die Originaldaten nicht mehr aus den transformierten Daten rekonstruieren kann. 

                    Durch die Verwendung einer inversen Transformation kann der legale Benutzer aus den verschlüsselten Daten die Originaldaten wieder regenerieren. 

                    Die Originaldaten werden mit „Klartext“ bezeichnet, die transformierten Daten werden mit dem Begriff „Schlüsseltext“ bezeichnet. 

                    Die Verschlüsselung von Daten kommt zunehmend im Alltag zum Einsatz. So verwenden viele Smartphones (z.B. Apple Iphone)eine automatische Verschlüsselung von Anwenderdaten zum Schutz dieser.

                    Außerdem wird bei besonders geheimen und hochsicheren Anforderungen eine Echtzeit-Verschlüsselung (Hardware-Verschlüsselung) von Telekommunikationsdaten verwendet.
                </p>
                </div>
